fun readLocalProperties(): Properties {
    val properties = Properties()
    val localPropertiesFile = rootProject.file("local.properties")
    if (localPropertiesFile.exists()) {
        properties.load(localPropertiesFile.inputStream())
    }
    return properties
}

val localProperties = readLocalProperties()

fun getFlutterVersionCode(): Int {
    return localProperties.getProperty("flutter.versionCode")?.toInt() ?: 1
}

fun getFlutterVersionName(): String {
    return localProperties.getProperty("flutter.versionName") ?: "1.0"
}

